CREATE VIEW [dbo].[VW_FAIXA]
AS
SELECT DISTINCT IN_FAIXA AS FAIXA
FROM         dbo.TB_CONTRATO
WHERE     (IN_FAIXA IS NOT NULL)

GO

CREATE VIEW [dbo].[VW_INFORMATIVO]
AS
SELECT DISTINCT AN_INFORMATIVO AS INFORMATIVO
FROM         dbo.TB_CONTRATO
WHERE     (AN_INFORMATIVO IS NOT NULL)

GO

CREATE VIEW [dbo].[VW_TIPOCONTRATO]
AS
SELECT DISTINCT IN_TP_CONTRATO AS TIPOCONTRATO
FROM         dbo.TB_CONTRATO
WHERE     (IN_TP_CONTRATO IS NOT NULL)

GO
